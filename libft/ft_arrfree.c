#include <stdlib.h>

void	ft_arrfree(void ***arr)
{
	int		i;

	if (!arr || !*arr)
		return ;
	i = 0;
	while ((*arr)[i])
	{
		free((*arr)[i]);
		i++;
	}
	free(*arr);
}
